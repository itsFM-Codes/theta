static const int *table_for_piece(Piece piece) {
    switch (piece_type(piece)) {
        case PIECE_TYPE_PAWN:
            return PAWN_TABLE;
        case PIECE_TYPE_KNIGHT:
            return KNIGHT_TABLE;
        case PIECE_TYPE_BISHOP:
            return BISHOP_TABLE;
        case PIECE_TYPE_ROOK:
            return ROOK_TABLE;
        case PIECE_TYPE_QUEEN:
            return QUEEN_TABLE;
        case PIECE_TYPE_KING:
            return KING_TABLE;
        case PIECE_TYPE_NONE:
            return 0;
    }

    return 0;
}

int piece_square_value(Piece piece, int square) {
    const int *table = table_for_piece(piece);
    int table_square;

    if (table == 0 || square < 0 || square >= SQUARE_COUNT) {
        return 0;
    }

    table_square = square;

    if (piece_color(piece) == COLOR_WHITE) {
        table_square ^= 56;
    }

    return table[table_square];
}
